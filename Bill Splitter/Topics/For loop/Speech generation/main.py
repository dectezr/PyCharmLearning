numbers = list(input())
for char in numbers:
    if char == '1':
        print('one')
    elif char == '2':
        print('two')
    elif char == '3':
        print('three')
    elif char == '4':
        print('four')
    elif char == '5':
        print('five')
    elif char == '6':
        print('six')
    elif char == '7':
        print('seven')
    elif char == '8':
        print('eight')
    elif char == '9':
        print('nine')
    elif char == '0':
        print('zero')

